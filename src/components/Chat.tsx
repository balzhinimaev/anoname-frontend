import React, { useState, useEffect, useRef } from 'react';
import websocketService from '../services/websocket';
import { hapticFeedback } from '../utils/telegram';
import {
  WebSocketChatMessageReceived,
  WebSocketChatStartTyping,
  WebSocketChatStopTyping,
  WebSocketUser
} from '../types';

interface ChatProps {
  chatId: string;
  partnerInfo: {
    telegramId: string;
    gender: 'male' | 'female';
    age: number;
  };
  currentUser: WebSocketUser | null;
  onEndChat: () => void;
}

interface ChatMessage {
  id: string;
  content: string;
  timestamp: string;
  isFromMe: boolean;
  sender: WebSocketUser;
}

const Chat: React.FC<ChatProps> = ({ chatId, partnerInfo, currentUser, onEndChat }) => {
  const [messages, setMessages] = useState<ChatMessage[]>([]);
  const [messageInput, setMessageInput] = useState<string>('');
  const [isTyping, setIsTyping] = useState<boolean>(false);
  const [partnerTyping, setPartnerTyping] = useState<boolean>(false);
  const messagesEndRef = useRef<HTMLDivElement>(null);
  const typingTimeoutRef = useRef<NodeJS.Timeout | null>(null);
  const partnerTypingTimeoutRef = useRef<NodeJS.Timeout | null>(null);

  useEffect(() => {
    // –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ WebSocket
    websocketService.onChatMessage((data: WebSocketChatMessageReceived) => {
      if (data.chatId === chatId) {
        const newMessage: ChatMessage = {
          id: data.message._id,
          content: data.message.content,
          timestamp: data.message.timestamp,
          isFromMe: currentUser ? data.message.sender.telegramId === currentUser.telegramId : false,
          sender: data.message.sender
        };
        
        setMessages(prev => [...prev, newMessage]);
        
        // –¢–∞–∫—Ç–∏–ª—å–Ω–∞—è –æ–±—Ä–∞—Ç–Ω–∞—è —Å–≤—è–∑—å –¥–ª—è –≤—Ö–æ–¥—è—â–∏—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
        if (!newMessage.isFromMe) {
          hapticFeedback('light');
        }
      }
    });

    websocketService.onChatStartTyping((data: WebSocketChatStartTyping) => {
      if (data.chatId === chatId && currentUser && data.userId !== currentUser.telegramId.toString()) {
        setPartnerTyping(true);
        
        // –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Å—Ç–∞—Ä—ã–π —Å—Ç–æ—Ä–æ–∂–µ–≤–æ–π —Ç–∞–π–º–µ—Ä
        if (partnerTypingTimeoutRef.current) {
            clearTimeout(partnerTypingTimeoutRef.current);
        }
        
        // –°—Ç–∞–≤–∏–º –Ω–æ–≤—ã–π —Å—Ç–æ—Ä–æ–∂–µ–≤–æ–π —Ç–∞–π–º–µ—Ä –Ω–∞ 30 —Å–µ–∫—É–Ω–¥
        partnerTypingTimeoutRef.current = setTimeout(() => {
            setPartnerTyping(false);
        }, 30000);
      }
    });

    websocketService.onChatStopTyping((data: WebSocketChatStopTyping) => {
      if (data.chatId === chatId && currentUser && data.userId !== currentUser.telegramId.toString()) {
        setPartnerTyping(false);
        
        // –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Å—Ç–æ—Ä–æ–∂–µ–≤–æ–π —Ç–∞–π–º–µ—Ä, —Ç.–∫. –ø–æ–ª—É—á–∏–ª–∏ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ –æ–± –æ—Å—Ç–∞–Ω–æ–≤–∫–µ
        if (partnerTypingTimeoutRef.current) {
            clearTimeout(partnerTypingTimeoutRef.current);
        }
      }
    });

    // –û—á–∏—Å—Ç–∫–∞ –ø—Ä–∏ —Ä–∞–∑–º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏
    return () => {
      if (typingTimeoutRef.current) {
        clearTimeout(typingTimeoutRef.current);
      }
      if (partnerTypingTimeoutRef.current) {
        clearTimeout(partnerTypingTimeoutRef.current);
      }
    };
  }, [chatId, currentUser]);

  useEffect(() => {
    // –ü—Ä–æ–∫—Ä—É—Ç–∫–∞ –∫ –ø–æ—Å–ª–µ–¥–Ω–µ–º—É —Å–æ–æ–±—â–µ–Ω–∏—é
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  }, [messages]);

  const handleSendMessage = (): void => {
    if (!messageInput.trim()) return;

    try {
      // –ù–µ–º–µ–¥–ª–µ–Ω–Ω–æ —Å–±—Ä–∞—Å—ã–≤–∞–µ–º —Ç–∞–π–º–µ—Ä –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º stop_typing
      if (typingTimeoutRef.current) {
        clearTimeout(typingTimeoutRef.current);
      }
      if (isTyping) {
        setIsTyping(false);
        websocketService.sendStopTyping(chatId);
      }
      
      websocketService.sendMessage(chatId, messageInput.trim());
      setMessageInput('');
      hapticFeedback('light');
    } catch (error) {
      console.error('–û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è:', error);
      hapticFeedback('error');
    }
  };

  const handleInputChange = (e: React.ChangeEvent<HTMLInputElement>): void => {
    const value = e.target.value;
    setMessageInput(value);

    // –ï—Å–ª–∏ –º—ã –µ—â–µ –Ω–µ –æ—Ç–ø—Ä–∞–≤–ª—è–ª–∏ —Å—Ç–∞—Ç—É—Å "–ø–µ—á–∞—Ç–∞–µ—Ç", –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –µ–≥–æ
    if (!isTyping && value.length > 0) {
      setIsTyping(true);
      websocketService.sendStartTyping(chatId);
    }
    
    // –°–±—Ä–∞—Å—ã–≤–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Ç–∞–π–º–µ—Ä –Ω–∞ –æ—Ç–ø—Ä–∞–≤–∫—É "stop_typing"
    if (typingTimeoutRef.current) {
      clearTimeout(typingTimeoutRef.current);
    }

    // –ï—Å–ª–∏ –ø–æ–ª–µ –≤–≤–æ–¥–∞ –ø—É—Å—Ç–æ–µ, —Å—Ä–∞–∑—É –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º "stop_typing"
    if (value.length === 0 && isTyping) {
      setIsTyping(false);
      websocketService.sendStopTyping(chatId);
      return;
    }
    
    // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –Ω–æ–≤—ã–π —Ç–∞–π–º–µ—Ä –Ω–∞ 1 —Å–µ–∫—É–Ω–¥—É
    typingTimeoutRef.current = setTimeout(() => {
      setIsTyping(false);
      websocketService.sendStopTyping(chatId);
    }, 1000); // 1 —Å–µ–∫—É–Ω–¥–∞ –±–µ–∑–¥–µ–π—Å—Ç–≤–∏—è
  };

  const handleKeyPress = (e: React.KeyboardEvent<HTMLInputElement>): void => {
    if (e.key === 'Enter') {
      e.preventDefault();
      handleSendMessage();
    }
  };

  const handleEndChat = (): void => {
    try {
      websocketService.endChat(chatId, 'user_ended');
      onEndChat();
      hapticFeedback('warning');
    } catch (error) {
      console.error('–û—à–∏–±–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è —á–∞—Ç–∞:', error);
    }
  };

  const formatTime = (timestamp: string): string => {
    const date = new Date(timestamp);
    return date.toLocaleTimeString('ru-RU', { 
      hour: '2-digit', 
      minute: '2-digit' 
    });
  };

  const SendIcon = () => (
    <svg viewBox="0 0 24 24" fill="currentColor">
      <path d="M2.01 21L23 12 2.01 3 2 10l15 2-15 2z" />
    </svg>
  );

  return (
    <div className="chat-container">
      {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ —á–∞—Ç–∞ */}
      <div className="chat-header">
        <div className="chat-header-info">
          {partnerInfo.gender === 'male' ? 'üë® –ú—É–∂—Å–∫–æ–π' : 'üë© –ñ–µ–Ω—Å–∫–∏–π'}, {partnerInfo.age} –ª–µ—Ç
        </div>
        <button 
          onClick={handleEndChat}
          className="end-chat-button"
        >
          –ó–∞–≤–µ—Ä—à–∏—Ç—å
        </button>
      </div>

      {/* –û–±–ª–∞—Å—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–π */}
      <div className="messages-area">
        {messages.length === 0 && (
          <div className="messages-area-placeholder">
            –û—Ç–ø—Ä–∞–≤—å—Ç–µ –ø–µ—Ä–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ üëã
          </div>
        )}

        {messages.map((message) => (
          <div
            key={message.id}
            className={`message-row ${message.isFromMe ? 'from-me' : ''}`}
          >
            <div
              className={`message-bubble ${message.isFromMe ? 'from-me' : 'from-partner'}`}
            >
              <div>{message.content}</div>
              <div className="message-time">
                {formatTime(message.timestamp)}
              </div>
            </div>
          </div>
        ))}

        {partnerTyping && (
          <div className="typing-indicator">
            <div className="typing-indicator-bubble">
              –°–æ–±–µ—Å–µ–¥–Ω–∏–∫ –ø–µ—á–∞—Ç–∞–µ—Ç...
            </div>
          </div>
        )}
        <div ref={messagesEndRef} />
      </div>

      {/* –ü–æ–ª–µ –≤–≤–æ–¥–∞ */}
      <div className="input-area">
        <input
          type="text"
          value={messageInput}
          onChange={handleInputChange}
          onKeyPress={handleKeyPress}
          placeholder="–í–≤–µ–¥–∏—Ç–µ —Å–æ–æ–±—â–µ–Ω–∏–µ..."
          className="message-input"
        />
        <button
          onClick={handleSendMessage}
          disabled={!messageInput.trim()}
          className="send-button"
        >
          <SendIcon />
        </button>
      </div>
    </div>
  );
};

export default Chat; 